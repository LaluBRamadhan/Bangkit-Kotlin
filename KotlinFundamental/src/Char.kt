fun main(){

    //contoh penggunaan variabel dengan tipe data char
    //char hanya bisa menampug satu huruf
    //contoh yang benar 'A' dan yang salah atau bakal error 'ABC' 
    var character: Char = 'A'
    println(character)

    //char bisa juga diberikan increment dan decrement
    var character2: Char = 'A'
    character2++
    println(character2)
    character2--
    println(character2)
    
}